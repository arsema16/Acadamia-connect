let currentLang = localStorage.getItem('lang') || 'en';

function t(key) {
  const val = (translations[currentLang] && translations[currentLang][key]) || translations['en'][key];
  if (val === undefined) {
    // Log missing key for debugging
    if (typeof console !== 'undefined') console.warn(`[i18n] Missing translation key: "${key}" for lang: ${currentLang}`);
    return key; // Fallback to key itself
  }
  return val;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function getMotivationalQuote() {
  const quotes = translations[currentLang].motivationalQuotes || translations.en.motivationalQuotes;
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((now - start) / 86400000);
  return quotes[dayOfYear % quotes.length];
}
